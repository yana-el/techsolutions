    Une panne majeure frappe Cloudflare ce midi, rendant inaccessibles de nombreux sites et services en ligne. De X à Feedly en passant par Marmiton ou Doctissimo, les utilisateurs signalent des erreurs de chargement, des pages inaccessibles et des ralentissements massifs. L’origine exacte du dysfonctionnement reste pour l’heure inconnue. <br>
   <br> Cloudflare, pilier de la sécurité et de la performance du web, assure habituellement la protection contre les attaques DDoS, l’optimisation du trafic et la disponibilité de millions de sites. Sa défaillance provoque mécaniquement une onde de choc planétaire: lorsqu’un acteur aussi central tombe, ce sont des pans entiers d’Internet qui deviennent inaccessibles.<br>
<br>
    Contactez par Tech&Co, Cloudflare, par la voix de Jackie Dutton, son directeur de la cybersécurité, nous explique que la société "a observé un pic inhabituel de trafic sur un des services de Cloudflare à 12h20", heure française.<br>
<br>
    Cette explosion momentanée de la charge a abouti à ce qu'une partie du trafic traversant le réseau de Cloudflare a rencontré des erreurs. "Nous ne savons pas encore ce qui a causé ce pic de trafic inhabituel. Nous sommes tous mobilisés pour faire en sorte que le trafic soit assuré sans erreur." L'urgence première étant de restaurer l'activité. Ensuite, "nous nous pencherons sur la cause de ce pic de trafic", conclut Jackie Dutton.
